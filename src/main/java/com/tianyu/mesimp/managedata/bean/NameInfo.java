package com.tianyu.mesimp.managedata.bean;

public class NameInfo {

	// 
	private String id;
	// 企业名称
	private String enterpriseName;
	// 所属区域
	private String area;
	// 地址
	private String address;
	// 组织机构代码证号
	private String enterpriseCode;
	// 联系人
	private String contacts;
	// 联系电话
	private String telephone;
	// 所属产业类别
	private String industryType;
	// 企业生产经营情况
	private String managementSituation;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEnterpriseName() {
		return enterpriseName;
	}
	public void setEnterpriseName(String enterpriseName) {
		this.enterpriseName = enterpriseName;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getEnterpriseCode() {
		return enterpriseCode;
	}
	public void setEnterpriseCode(String enterpriseCode) {
		this.enterpriseCode = enterpriseCode;
	}
	public String getContacts() {
		return contacts;
	}
	public void setContacts(String contacts) {
		this.contacts = contacts;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getIndustryType() {
		return industryType;
	}
	public void setIndustryType(String industryType) {
		this.industryType = industryType;
	}
	public String getManagementSituation() {
		return managementSituation;
	}
	public void setManagementSituation(String managementSituation) {
		this.managementSituation = managementSituation;
	}
	
	
}
